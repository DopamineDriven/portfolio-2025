export type InferSplitOutput<T> = T extends `${infer U}:${infer V}:${infer W}`
  ? [U, V, W]
  : T;

export function exciseColons<const T extends `${string}:${string}:${string}`>(
  str: T
): InferSplitOutput<T> {
  return str.split(/:/g) as InferSplitOutput<typeof str>;
}

